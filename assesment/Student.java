public class Student {
	 int id, mark;
	 String name;
	 String course;

	public void setId(int id) {
		this.id = id;
	}

	public void setMark(int mark) {
		this.mark = mark;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setCourse(String course) {
		this.course = course;
	}

	public int getId() {
		return id;
	}

	public int getMark() {
		return mark;
	}

	public String getName() {
		return name;
	}

	public String getCourse() {
		return course;
	}
}
	
